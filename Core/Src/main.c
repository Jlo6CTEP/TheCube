void HAL_SYSTICK_Callback() {
    uint8_t pin = HAL_GPIO_ReadPin(GPIOD, GPIO_PIN_14);

    if (pin == GPIO_PIN_RESET) {
        on_duration = 0;
        off_duration++;

        if (off_duration > DEBOUNCE_MS && !button_pressed) {
            off_duration = DEBOUNCE_MS + 1;
            button_pressed = true;
            button_press_cleared = false;
        }
    } else {
        off_duration = 0;
        on_duration++;

        if (on_duration > DEBOUNCE_MS) {
            on_duration = DEBOUNCE_MS + 1;
            button_pressed = false;
        }
    }

    if (!button_pressed || button_press_cleared) {
        return;
    }

    if (status == WORKING) {
        status = SLEEPING;
    } else if (Status == SLEEPING){
        status = WORKING;
    }
    button_press_cleared = true;
}

uint8_t endswith(const char *str, const char *suffix) {
    if (!str || !suffix)
        return 0;
    size_t str_len = strlen(str);
    size_t suffix_len = strlen(suffix);
    if (suffix_len > str_len)
        return 0;
    return strncmp(str + str_len - suffix_len, suffix, suffix_len) == 0;
}

uint8_t load_prog(Program *program, char *filename) {
    uint8_t fresult;
    fresult = f_open(&fil, filename, FA_OPEN_ALWAYS | FA_READ);

    if (fresult != FR_OK) {
        return SD_READ_ERROR;
    }

    unsigned int size_read = 0;
    ProgramParameters prog_param = {0};
    f_read(&fil, &prog_param, sizeof(ProgramParameters), &size_read);

    for (uint32_t i = 0; i < prog_param.prog_len; i++) {
        f_read(&fil, program->frames[i].leds, program->led_count * sizeof(LED), &size_read);
        if (size_read != program->led_count * sizeof(LED)) {
            f_close(&fil);
            return SD_READ_ERROR;
        }
        f_read(&fil, &program->frames[i].is_interpolated, 2 * sizeof(uint8_t) + sizeof(uint16_t), &size_read);
        if (size_read != 2 * sizeof(uint8_t) + sizeof(uint16_t)) {
            f_close(&fil);
            return SD_READ_ERROR;
        }
        program->frames[i].interpolation_steps *= 3;
    }
    program->program_len = prog_param.prog_len;
    f_close(&fil);
    return NO_ERROR;
}
